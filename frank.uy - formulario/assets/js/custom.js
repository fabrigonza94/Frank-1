$("#show-more").click(verDetalle);
    function verDetalle () {
    if ($(".info-hide").css("display")=="none") 
    {
        $(".info-hide").slideDown("slow");
    } else {
        $(".info-hide").slideUp("slow");
    }
    $(this).hide();
}






$('.carousel[data-type="multi"] .item').each(function() {
	var next = $(this).next();
	if (!next.length) {
		next = $(this).siblings(':first');
	}
	next.children(':first-child').clone().appendTo($(this));

	for (var i = 0; i < 2; i++) {
		next = next.next();
		if (!next.length) {
			next = $(this).siblings(':first');
		}

		next.children(':first-child').clone().appendTo($(this));
    }
});
